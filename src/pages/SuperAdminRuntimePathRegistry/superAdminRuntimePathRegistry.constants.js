export const RUNTIME_PATH_REGISTRY_PAGE_SIZE = 20

const normalizeStableKeySegment = (value) =>
  String(value || '')
    .trim()
    .replace(/[^a-z0-9-]+/gi, '-')
    .replace(/^-+|-+$/g, '')
    .toLowerCase()

export const buildRuntimePathRegistryStableId = (pathKey) =>
  `path-${normalizeStableKeySegment(pathKey).slice(0, 160)}`

const withDerivedRuntimePathRegistryId = (entry) => ({
  ...entry,
  id: buildRuntimePathRegistryStableId(entry.pathKey),
})

